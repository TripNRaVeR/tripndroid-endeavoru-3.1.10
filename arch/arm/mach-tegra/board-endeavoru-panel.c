static void bkl_do_work(struct work_struct *work)
{
	struct backlight_device *bl = platform_get_drvdata(&enterprise_disp1_backlight_device);
	if (bl) {
		bl->props.bkl_on = 1;
		backlight_update_status(bl);
	}
}

