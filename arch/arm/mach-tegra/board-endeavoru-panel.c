static int bkl_calibration_get(void *data, u64 *val)
{
	return 0;
}

static int bkl_calibration_set(void *data, u64 val)
{
	struct backlight_device *bl = platform_get_drvdata(&enterprise_disp1_backlight_device);
	switch (val) {
		case CAB_LOW:
			def_pwm = MAP_PWM_LOW_DEF;
		break;
		case CAB_HIGH:
			def_pwm = MAP_PWM_HIGH_DEF;
		break;
		case CAB_DEF:
		default:
			def_pwm = MAP_PWM_DEF;
	}
	backlight_update_status(bl);

	return 0;
}

